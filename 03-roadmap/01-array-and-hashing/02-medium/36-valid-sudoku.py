from collections import defaultdict


def isValidSudoku(board):
    rows = defaultdict(set)
    columns = defaultdict(set)
    sudoku_table = defaultdict(set)  # key = (r/3, c/3)

    for i in range(9):
        for j in range(9):
            if board[i][j] == ".":
                continue

            if (
                board[i][j] in rows[i]
                or board[i][j] in columns[j]
                or board[i][j] in sudoku_table[(i // 3, j // 3)]
            ):
                return False

            rows[i].add(board[i][j])
            columns[j].add(board[i][j])
            sudoku_table[(i // 3, j // 3)].add(board[i][j])

    return True


